package exam07_4_method_polymorphism;
//매개변수의 다형성 실행 클래스
public class DriverExample {

	public static void main(String[] args) {
		Driver driver = new Driver();
		
		Bus bus = new Bus();
		Taxi taxi = new Taxi();
		
		driver.drive(bus);	//자동타입변환 : Vehicle vehicle = bus;
		driver.drive(taxi);	//자동타입변환

	}

}
